package io.github.nightlyside.enstaunofficialguide.data_structure;

public class News {
    public String title;
    public String date;
    public String author;
    public String text;

    public News(String title, String date, String author, String text) {
        this.title = title;
        this.date = date;
        this.author = author;
        this.text = text;
    }
}
